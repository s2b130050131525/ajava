
package myPackage;


public class User_Info {

    private String name;
    private String password;

    public User_Info() {
        name = null;
        password=null;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

   
    public String getPassword() {
        return password;
    }

   
    public void setPassword(String password) {
        this.password = password;
    }

}
